package com.mvc.service;

public class Addservice {

	public float add(float first_num, float second_num) {

		return first_num + second_num;

	}

	public float subtract(float first, float second) {

		return first - second;
	}

	public float multiply(float first_no, float second_no) {

		return first_no * second_no;
	}
	
	public float division(float first_number, float second_number) {

		return first_number/second_number;
	}
}


